MYSQL CODE

ACCOUNT:
create table ACCOUNT( 
ACC_USERNAME VARCHAR(100) NOT NULL PRIMARY KEY, 
ACC_FNAME VARCHAR(25) NOT NULL, 
ACC_LNAME VARCHAR(25) NOT NULL, 
ACC_SUFIX VARCHAR(20), 
ACC_MIDDLE VARCHAR(75), 
ACC_MAIDEN VARCHAR(75), 
ACC_PREFIX VARCHAR(20), 
ACC_HONNARY VARCHAR(50), 
ACC_DOB DATE, 
ACC_SCHOOLID VARCHAR(20) NOT NULL);


EMAIL:
CREATE TABLE EMAIL( 
EMA_ADDRESS VARCHAR(100) PRIMARY KEY, 
ACC_USERNAME VARCHAR(100) NOT NULL, 
FOREIGN KEY (ACC_USERNAME) REFERENCES ACCOUNT(ACC_USERNAME));


INSTRUCTOR:
CREATE TABLE INSTRUCTOR( 
ACC_USERNAME VARCHAR(20) PRIMARY KEY, 
ACC_FNAME VARCHAR(25) NOT NULL, 
ACC_LNAME VARCHAR(25) NOT NULL, 
FOREIGN KEY (ACC_USERNAME) REFERENCES ACCOUNT(ACC_USERNAME));


